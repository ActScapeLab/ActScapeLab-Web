// 卒論 or 修論を判別
export function getThesisType(grade:string) {
  if (grade == 'B4'){
    return '卒論'
  }
  else {
    return '修論'
  }
}

export function getLink(link?:string, kind?:string) {
  if (link == undefined) {
    return undefined
  }
  else if (kind == 'staff'){
    return `member/staff/${link}`
  }

  else {
    return `member/current/${link}`
  }
}